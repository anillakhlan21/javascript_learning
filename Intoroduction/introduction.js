/* 
* JavaScript is an interpreted language
JavaScript is an interpreted language. We cannot compile a JavaScript program into an executable. 
We distribute it as plain text.

To run an interpreted language, the client machines require an Interpreter. 
The Interpreters read the program line by line and execute each command. 
The Interpreted languages were once significantly slower than compiled languages.

Every browser comes with a built-in Interpreter in the form of a Javascript virtual machine
*/

/* 
* Javascript virtual machine
The JavaScript virtual machine (JVM) is the component of the browser that reads our JavaScript code, 
optimizes, and executes it.

The job of JVM is to Interpret the JavaScript code and run it. Each browser comes with a version 
of JVM. The following are some of the popular JVM

V8 — Developed by Google for chrome.
Rhino — Developed by Mozilla Foundation for Firefox
SpiderMonkey — The first JavaScript engine, used by Netscape Navigator, and today powers Firefox
JavaScriptCore — Developed by Apple for Safari
Chakra — Developed by Microsoft and runs on Microsoft Edge
JerryScript — is a lightweight engine for the IoT devices
*/


