/*
* What is Javascript ?
Javascript is the scripting language, we use to make web pages interactive. 
It is written in plain text on the HTML page and runs in the browser. 
Modern JavaScript evolved so much that, it can run on the server side today. 
We use it along with HTML & CSS and powers the entire web.
*/

/*
* History
JavaScript was developed by Brendan Eich of Netscape in 1995. 
It started out as Mocha, then became LiveScript and later JavaScript.

Java was a very popular language at that time. 
Hence the marketing folks at Netscape decided to name it JavaScript so as to encash the 
popularity of Java. They also designed it so that its syntax looks similar to that of Java

Microsoft included support for JavaScript (named JScript) from Internet Explorer 3.0. 
It also included its own scripting language VBScript. 
JavaScript was also incorporated by other browsers, while VBScript remained only with Internet
 Explorer. This eventually led to the demise of VBScript.

The browser vendors started to come out with their own implementation of JavaScript. 
Since there was no standard, each browser was free to implement as they choose. 
Hence the need for standardizing the syntax & rules of JavaScript.

In 1997. Netscape presented JavaScript to ECMA International, which standardized the specifications 
of the JavaScript under the name ECMAScript specifications (pronounced as “ek-ma-script”). 
The browsers now implement Javascript using ECMAScript specifications, which is why the JavaScript 
programs behave similarly across all browsers.
*/


/*
* How it is Used
We use JavaScript to create and manage dynamic web pages. It works side by side with HTML & CSS.

The HTML provides the content & structure of the web pages. 
You can create content using headings, paragraphs, tables & images, etc.

The CSS controls how the Web looks like. You can set color, font styles, backgrounds, borders, etc.

JavaScript makes it dynamic. Using JavaScript, you can respond to user events, 
Take input from the user, Validate those inputs, run business logic, read and send data to the 
servers, etc. It can also alter the content & look by manipulating the HTML & CSS on the fly.
*/


